<template>
  <div>
    <h2>返校统计页面</h2>
    <div class="add">
      <p>文件导出部分</p>
      <form @submit="onSubmit">
        <input type="password" v-model="title" placeholder="请输入密码">
        <input type="submit" value="submit">
      </form>
    </div>
  </div>
</template>

<script>
import { mapActions } from "vuex";
export default {
  name: "AddTodo",
  data() {
    return {
      title: ""
    };
  },
  methods: {
    ...mapActions(["addTodo"]),
    onSubmit(e) {
      e.preventDefault();
      this.addTodo(this.title);
    }
  }
};
</script>

